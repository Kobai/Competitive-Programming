import java.util.Scanner;


public class What_Is_N {

	public static void main(String[] args) {
		Scanner input = new Scanner(System.in);
		
		int num = input.nextInt();
	
		
		switch(num)
		{
		case 1:
			System.out.println("1");
			break;
		case 2:
			System.out.println("2");
			break;
		case 3:
			System.out.println("2");
			break;
		case 4:
			System.out.println("3");
			break;
		case 5:
			System.out.println("3");
			break;
		case 6:
			System.out.println("3");
			break;
		case 7:
			System.out.println("2");
			break;
		case 8:
			System.out.println("2");
			break;
		case 9:
			System.out.println("1");
			break;
		case 10:
			System.out.println("1");
			break;
			
		}
	}
}
